package tryFunctions;

public class Solution {
public static void main(String[] args) {
	int a=5;
	int b=3;
	int c= Math.min(a, b);
	int d= Math.max(c, Math.min(a, b));
	int e= Math.abs(a);
	double f= Math.pow(a, b);
	double g= Math.log(a);
	double k= Math.exp(a);
	System.out.println(a+" "+b+" "+c+" "+d+" "+e+" "+f+" "+g+" "+k);
	
}
}
